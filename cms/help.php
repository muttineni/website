<h2>QuickForm Toolbar</h2>
<p>The purpose of the QuickForm is to reduce the amount of work necessary to enter resources into the database.</p>
<p>The QuickForm Toolbar helps to clean up the text in the QuickForm text box.</p>
<ul>
  <li><b>Strip White Space:</b> Remove blank lines from the contents of the text box. Useful for cleaning up the QuickForm text box when listing glossary terms and definitions.</li>
  <li><b>Add Dashes:</b> Remove blank lines from the contents of the text box and add a dash to the beginning of each line. Useful for cleaning up the QuickForm text box when listing questions and answers for an exercise.</li>
  <li><b>Remove Newlines:</b> Remove all line breaks from highlighted text. Useful for condensing a multi-line question into a single line for an exercise.</li>
  <li><b>Copy Text:</b> Highlight and copy all text in the text box.</li>
</ul>
