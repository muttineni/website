<a name="formatting-toolbar"></a>
<h2>Formatting Toolbar</h2>
<ul>
<li>Listed below are all of the formatting tags available.</li>
<li>Only the following tags have buttons in the formatting toolbar: paragraph, bold, italic, underline, blue, red, large, small, e-mail, link, link w/ desc, bulleted list, numbered list.</li>
<li>
  For tags with buttons in the formatting toolbar:
  <ul>
    <li>If you click a button with no text highlighted, you will be asked what text you would like to format.  After entering the text and clicking &quot;OK&quot;, the formatted text will be added to the end of the text box.</li>
    <li>If you highlight text in the text box and then click a button, the formatting tags will be added to the highlighted text.</li>
    <li>With the bulleted list and numbered list buttons, if you highlight multiple lines and then click the button, each line will become an item in the list.</li>
  </ul>
</li>
