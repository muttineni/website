<h2>QuickForm</h2>
<p>The QuickForm field can be used for creating the following <b>Course Resources</b>:</p>
<ul>
  <li>
    <b>Course Introduction:</b>
    <ul>
      <li>Enter course introduction in the Content field</li>
      <li>Enter all of the course objectives in the QuickForm field</li>
      <li>Each objective should be on a separate line with no blank lines in between</li>
      <li><b>Note:</b> After the objectives have been created, they will no longer show up in the QuickForm field. Instead, each objective will have its own &quot;Edit&quot; link on the course outline page.</li>
    </ul>
  </li>
  <li>
    <b>Course Objectives:</b>
    <ul>
      <li>Enter all of the course objectives in the QuickForm field</li>
      <li>Each objective should be on a separate line with no blank lines in between</li>
      <li><b>Note:</b> After the objectives have been created, they will no longer show up in the QuickForm field. Instead, each objective will have its own &quot;Edit&quot; link on the course outline page.</li>
    </ul>
  </li>
  <li>
    <b>Glossary:</b>
    <ul>
      <li>Enter all glossary terms and definitions in the QuickForm field</li>
      <li>Each term should be on a separate line followed immediately by that term's definition with no blank lines in between</li>
      <li>Continue in this manner for all terms and definitions (term 1, definition 1, term 2, definition 2, etc.)</li>
      <li><b>Note:</b> After the glossary has been created, the glossary terms and definitions will no longer show up in the QuickForm field. Instead, when editing the glossary, all terms and definitions will show up in the Content field (with &quot;dl&quot;, &quot;dt&quot;, and &quot;dd&quot; tags).</li>
    </ul>
  </li>
</ul>
<p>The QuickForm field can be used for creating the following <b>Session Resources</b>:</p>
<ul>
  <li>
    <b>Session Introduction:</b>
    <ul>
      <li>Enter session introduction in the Content field</li>
      <li>Enter all of the session objectives in the QuickForm field</li>
      <li>Each objective should be on a separate line with no blank lines in between</li>
      <li><b>Note:</b> After the objectives have been created, they will no longer show up in the QuickForm field. Instead, each objective will have its own &quot;Edit&quot; link on the course outline page.</li>
    </ul>
  </li>
  <li>
    <b>Session Objectives:</b>
    <ul>
      <li>Enter all of the session objectives in the QuickForm field</li>
      <li>Each objective should be on a separate line with no blank lines in between</li>
      <li><b>Note:</b> After the objectives have been created, they will no longer show up in the QuickForm field. Instead, each objective will have its own &quot;Edit&quot; link on the course outline page.</li>
    </ul>
  </li>
  <li>
    <b>Exercises:</b>
    <ul>
      <li>Enter all questions and answer choices in the QuickForm field</li>
      <li>Each question should be on a separate line followed immediately by that question's answer choices with no blank lines in between</li>
      <li>Each answer choice should be on a separate line</li>
      <li>If the answer choice is an incorrect answer or if it is unknown which answer choice is correct, the line should begin with a minus sign (&quot;-&quot;)</li>
      <li>If the answer choice is the correct answer, the line should begin with a plus sign (&quot;+&quot;)</li>
      <li>For questions with no answer choices (such as essay questions), enter the question with no blank lines following it</li>
      <li>Use the &quot;Add Dashes&quot; button on the QuickForm toolbar to add a dash (minus sign) to the beginning of each line in the QuickForm field</li>
      <li>Questions should not have a minus or plus sign at the beginning of the line</li>
      <li><b>Note:</b> After the questions and answers have been created, they will no longer show up in the QuickForm field.</li>
    </ul>
  </li>
</ul>
